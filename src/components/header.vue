        $('.CartSkuSelect_box').css('display', 'none')
        $('.CartSkuSelect_mask').css('display', 'none')
        $('.CartSkuSelect').css('display', 'none')
        $('.CartSkuSelect_loading').css('display', 'none')
        $('.CartSkuSelect_loading').css('display', 'none')
      }, 200);

    },
    getAllPrice () {
      let cartGoods = this.cartGoods
      let total = 0
      cartGoods.map(v => {
        total = Number(v.allPrice) + total
      })
      this.total = total.toFixed(2)
    },
    getItem () {
      let cartGoods = this.cartGoods
      console.log('getitem cartGoods', cartGoods)
      let count = 0
      cartGoods.map(v => {
        count = Number(v.count + count)
      })
      if (cartGoods) {
        this.$store.commit('getCartGoods_length', count)
      }

    },
    toIndex () {
      this.$router.push('/')
    },
    toCheckOut () {
      this.$router.push('checkout')
    },
    toLogin () {
      this.$router.push('login')
    }
