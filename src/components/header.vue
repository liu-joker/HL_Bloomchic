export default {
  inject: ['reload'],
  data () {
    return {
      cart_drawer: false,
      cartGoods_length: 0,
      total: 0,
      choose_item: '',
      choose_item_from: '',
      size_index: 0,
      color_index: 0,
      cartGoods: []

    };
  },
  created () {
    let cartGoods = JSON.parse(sessionStorage.getItem('goodsCart')) || []
    this.cartGoods = cartGoods
    this.getItem()
  },
  methods: {
    chooseSize (item, index, type) {
      // this.choose_item_from = JSON.parse(JSON.stringify(this.choose_item))
      if (type == 'color') {
        this.choose_item_from.color = item.color
        console.log('his.choose_item_from.color', this.choose_item_from.color, this.choose_item_from.size)
        return

      }
      if (type == 'size') {
        this.choose_item_from.size = item
        console.log('his.choose_item_from.color', this.choose_item_from.color, this.choose_item_from.size)
        return
      }

    },
    toCategory (cateId) {
      this.$router.push('/category?cateId=' + cateId)
      this.reload()
    },
    upDate () {
      let id = this.choose_item_from.id
      let choose_item = this.choose_item
      let choose_item_index
      let color = this.choose_item.color
      let size = this.choose_item.size
      let current_img = ''
      this.cartGoods.map((v, index) => {
        if (v.id == id && color == v.color && size == v.size) {
          choose_item_index = index
          v.variants.map(c => {
            if (c.option1 == this.choose_item_from.color && c.option2 == this.choose_item_from.size) {
              console.log('variants', c)
              this.choose_item_from.images.map(i => {
                if (i.id == c.image_id) {
                  console.log('current_img', i.src)
                  current_img = i.src
                  return
                }
              })
            }
          })
          return
        }
      })

      this.choose_item_from.image.src = current_img
      // console.log('choose_item', this.choose_item_from)
      this.cartGoods.splice(choose_item_index, 1, this.choose_item_from)
      this.hideCartAlert()
      this.$store.commit('getCartGoods', this.cartGoods)
      sessionStorage.setItem('goodsCart', JSON.stringify(this.cartGoods))
      this.getItem()
      this.getAllPrice()
      this.choose_item = ''
    },
    addItem (v, index) {
      v.count = v.count + 1
      v.allPrice = (v.count * Number(v.price)).toFixed(2)
      console.log('kkkkkkkk')
      sessionStorage.setItem('goodsCart', JSON.stringify(this.cartGoods))
      this.$store.commit('getCartGoods', JSON.stringify(this.cartGoods))
      this.getItem()
      this.getAllPrice()
    },
    cutItem (v, index) {
      v.count = v.count - 1
      if (v.count == 0) {
        this.cartGoods.splice(index, 1)
      } else {
        v.allPrice = (v.count * Number(v.price)).toFixed(2)
      }
      sessionStorage.setItem('goodsCart', JSON.stringify(this.cartGoods))
      this.$store.commit('getCartGoods', this.cartGoods)
      this.getItem()
      this.getAllPrice()
    },

    deleteItem (item) {
      this.cartGoods.map((v, index) => {
        if (v.id == item.id) {
          return this.cartGoods.splice(index, 1)
        }
      })
      sessionStorage.setItem('goodsCart', JSON.stringify(this.cartGoods))
      this.$store.commit('getCartGoods', this.cartGoods)
      this.getItem()
      this.getAllPrice()
    },
    showCartAlert (item) {
      this.choose_item = JSON.parse(JSON.stringify(item))
      this.choose_item_from = JSON.parse(JSON.stringify(this.choose_item))
      console.log('choose_item', this.choose_item)
